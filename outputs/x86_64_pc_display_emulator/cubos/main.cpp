void setup()
{ 
    driver_battery_setup();

    #ifdef USE_RTC
        driver_RTC_setup();
    #endif

    #ifdef serialDebug
        Serial.begin(115200);
        debug("Serial debug started");
    #endif

    #ifdef ESP8266
        ESP.wdtDisable();
    #endif
    
    driver_cpu_setup();
    
    setup_displayDriver();
    driver_controls_setup();
    
    currentApp = getApp(STARTING_APP_NUMM);
  
}

bool isInSleep = false;
void loop(){
  driver_controls_loop();
  driver_battery_loop();
  core_time_loop();
  currentApp->onLoop(); 
  //currentApp->onLoop(); 

  #ifdef ESP8266
    //ESP.wdtDisable();
    ESP.wdtFeed();
  #endif

  #ifdef CPU_SLEEP_ENABLE
//driver_cpu_sleep();
    if(millis() - driver_control_get_last_user_avtivity() > DELAY_BEFORE_SLEEP){
        if(!isInSleep){
            isInSleep = true;
            currentApp->onEvent(EVENT_ON_GOING_TO_SLEEP, 0, 0);
            powerOff_displayDriver();
        }

        do_cpu_sleep();
      
    }else{
      if(isInSleep){
        isInSleep = false;
        driver_cpu_wakeup();
        currentApp->onEvent(EVENT_ON_WAKE_UP, 0, 0);
      }
    }
    //driver_cpu_wakeup();
  #endif

}

